import axios from 'axios';
import authHeader from './auth-header';
import authHeaderMultiPart from "./auth-header-multi-part";
import {environment} from "./environment";
import qs from 'qs'

const API_URL = environment.ETL_URL;

class ETLService {

    /**
     * Sends POST request to API to create an ETL procedure
     *
     * @param ehrName EHR database name
     * @param file file containing Scan Report of the EHR database
     * @param cdm OMOP CDM version
     * @returns created ETL procedure created
     */

    createETL = (ehrName, file, cdm) => {
        const username = JSON.parse(localStorage.getItem('user')).username;

        let formData = new FormData();
        formData.append("file", file);

        try {
            return axios.post(
                API_URL + "procedures", formData,
                { headers: authHeaderMultiPart(), params: { ehrName: ehrName, cdm: cdm, username: username } }
            );
        } catch (e) {
            console.log(e);
        }
    }


    /**
     * Sends POST request to API to create an ETL procedure from a JSON file generated by backend
     *
     * @param file JSON file
     * @returns {Promise<AxiosResponse<any>>}
     */

    createETLFromFile = (file) => {
        let formData = new FormData();
        formData.append("file", file);
        const username = JSON.parse(localStorage.getItem('user')).username;

        try {
            return axios.post(
                API_URL + "procedures/save", formData,
                { headers: authHeaderMultiPart(), params: { username: username} }
            );
        } catch(e) {
            console.log(e);
        }
    }


    /**
     * Sends GET request to API to retrieve all ETL procedures (used by ADMIN)
     *  
     * @returns
     */

    getAllETL = () => {
        try {
            return axios.get(
                API_URL + 'procedures',
                { headers: authHeader() }
            )
        } catch (e) {
            console.log(e);
        }
    }


    /**
     * Sends an GET request to API to retrieve ETL procedures of a given user
     *
     * @returns {Promise<AxiosResponse<any>>}
     */

    getUserETL = () => {
        const username = JSON.parse(localStorage.getItem('user')).username;
        
        try {
            return axios.get(
                API_URL + "user_procedures",
                { headers: authHeader(), params: { username: username } });
        } catch (e) {
            console.log(e);
        }
    }


    /**
     * Sends GET request to API to retrieve procedure given its Id
     *
     * @param id ETL procedure's id
     * @returns re
     */

    getETLById = (id) => {
        const username = JSON.parse(localStorage.getItem('user')).username;
        try {
            return axios.get(
                API_URL + "procedures/" + id,
                { headers: authHeader(), params: { username: username } }
            );
        } catch (e) {
            console.log(e);
        }
    }


    /**
     * Sends a DELETE request to delete an ETL procedure (including databases, tables, fields and mappings)
     *
     * @param etl_id ETL procedure's id
     * @returns {Promise<AxiosResponse<any>>}
     */

    deleteETLProcedure(etl_id) {
        try {
            return axios.delete(
                API_URL + "procedures",
                { headers: authHeader(), params: { etl_id: etl_id } }
            );
        } catch(e) {
            console.log(e);
        }
    }


    /**
     * Sends a PUT request to API to change the ETL procedure name
     *
     * @param etl_id ETL procedure's id
     * @param name name to change to
     * @returns {Promise<AxiosResponse<any>>}
     */

    changeETLProcedureName(etl_id, name) {
        const username = JSON.parse(localStorage.getItem('user')).username;
        try {
            return axios.put(
                API_URL + "procedures/name",
                null,
                { headers: authHeader(), params: {etl_id: etl_id, name: name, username: username}}
            );
        } catch (e) { console.log(e) }
    }


    /**
     * Sends a PUT request to API to change the EHR database name
     *
     * @param sourceDatabaseId source database's id
     * @param etl_id ETL procedure's id
     * @param name name to change to
     * @returns {Promise<AxiosResponse<any>>}
     */

    changeEHRDatabaseName(sourceDatabaseId, etl_id, name) {
        const username = JSON.parse(localStorage.getItem('user')).username;
        try {
            return axios.put(
                API_URL + "ehrDatabase/name",
                null,
                {
                    headers: authHeader(),
                    params: {
                        sourceDatabaseId: sourceDatabaseId,
                        name: name,
                        etl_id: etl_id,
                        username: username
                    }
                }
            );
        } catch (e) { console.log(e) }
    }


    inviteCollaboratorsToETL(usersToInvite, etl_id) {
        const username = JSON.parse(localStorage.getItem('user')).username;
        try {
            return axios.put(
                API_URL + "invite",
                null,
                {
                    headers: authHeader(),
                    params: {
                        usersToInvite: usersToInvite,
                        etl_id: etl_id,
                        username: username
                    }
                }
            );
        } catch (e) { console.log(e) }
    }


    /**
     * Marks an ETL procedure as deleted (but doesn't remove it from database)
     *
     * @param etl_id ETL procedure's id
     * @returns {Promise<AxiosResponse<any>>}
     */

    markETLProcedureAsDeleted(etl_id) {
        const username = JSON.parse(localStorage.getItem('user')).username;
        try {
            return axios.put(
                API_URL + "procedures_del",
                null,
                { headers: authHeader(), params: { etl_id: etl_id, username: username }}
            );
        } catch(e) {
            console.log(e);
        }
    }


    /**
     * Marks an ETL procedure as not deleted (only by ADMIN)
     *
     * @param etl_id ETL procedure's id
     * @returns {Promise<AxiosResponse<any>>}
     */

    unmarkETLProcedureAsDeleted(etl_id) {
        const username = JSON.parse(localStorage.getItem('user')).username;
        try {
            return axios.put(
                API_URL + "procedures_undel",
                null,
                { headers: authHeader(), params: { etl_id: etl_id, username: username }}
            );
        } catch(e) {
            console.log(e);
        }
    }


    /**
     * Sends PUT request to change the OMOP CDM version
     *
     * @param etl ETL procedure
     * @param cdm CDM version to change to
     * @returns {Promise<AxiosResponse<any>>} ETL procedure with new data
     */

    changeTargetDatabase(etl_id, cdm) {
        const username = JSON.parse(localStorage.getItem('user')).username;
        return axios.put(
            API_URL + "procedures/targetDB", 
            null, 
            { headers: authHeader(), params:{ username: username, etl_id: etl_id, cdm: cdm }}
        );
    }


    /**
     * Add stem tables in both databases
     *
     * @param etl_id ETL procedure's id
     * @returns {Promise<AxiosResponse<any>>}
     */

    addStemTables(etl_id) {
        const username = JSON.parse(localStorage.getItem('user')).username;
        return axios.put(
            API_URL + "procedures/stem",
            null,
            { headers: authHeader(), params:{ etl_id: etl_id, username: username }}
        ).catch(e => console.log(e));
    }


    /**
     * Sends a PUT request to remove stem tables from both databases, including mappings
     *
     * @param etl_id ETL procedure's id
     * @returns {Promise<AxiosResponse<any>>}
     */

    removeStemTables (etl_id) {
        const username = JSON.parse(localStorage.getItem('user')).username;
        return axios.put(
            API_URL + "procedures/remove_stem",
            null,
            { headers: authHeader(), params:{ etl_id: etl_id, username: username }}
        );
    }


    /**
     * Sends GET request to retrieve data from source fields summary file
     * 
     * @param etl_id ETL procedure's id
     * @returns 
     */

    downloadSourceFieldsFile = async (etl_id) => {
        const username = JSON.parse(localStorage.getItem('user')).username;
        return await axios.get(
            API_URL + "procedures/sourceCSV",
            { headers: authHeader(), params: { etl_id: etl_id, username: username }}
        );
    }


    /**
     * Sends GET request to retrieve data from target fields summary file
     *
     * @param etl_id
     * @returns {Promise<AxiosResponse<any>>}
     */

    downloadTargetFieldsFile = async (etl_id) => {
        const username = JSON.parse(localStorage.getItem('user')).username;
        return await axios.get(
            API_URL + "procedures/targetCSV",
            { headers: authHeader(), params: { etl_id: etl_id, username: username }}
        )
    }


    /**
     * Sends GET request to retrieve data from table mappings summary file
     *
     * @param etl_id ETL procedure's id
     * @returns {Promise<AxiosResponse<any>>}
     */

    downloadSummaryFile(etl_id) {
        const username = JSON.parse(localStorage.getItem('user')).username;
        return axios.get(
            API_URL + "procedures/summary",
            { headers: authHeader(), params: { etl_id: etl_id, username: username }, responseType: "blob"}
        );
    }


    /**
     * Sends GET request to retrieve data from summary file
     *
     * @param etl_id
     * @returns {Promise<AxiosResponse<any>>}
     */

    downloadSaveFile(etl_id) {
        const username = JSON.parse(localStorage.getItem('user')).username;
        return axios.get(
            API_URL + "procedures/save",
            { headers: authHeader(), params: { etl_id: etl_id, username: username }}
        );
    }
}

export default new ETLService();